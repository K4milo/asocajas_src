        <h2>Hablemos de Protección Social en Colombia</h2>
        <p>Desde la creación del Sistema de Compensación Familiar, hace 65 años, hemos contribuido al cierre de brechas sociales y al mejoramiento de la calidad de vida de los trabajadores y sus familias, brindándole acceso a educación, salud, vivienda, turismo, cultura y recreación, entre otros. Por esto, conscientes de los retos que afronta el país en materia de Protección Social, nos unimos con el Ministerio del Trabajo para promover un análisis y debate sobre esta temática, que incide directamente en el bienestar de todos los colombianos.

Bajo este propósito realizaremos el próximo 29 de mayo el foro Hablemos de Protección Social en Colombia, un espacio en el que articularemos a altos funcionarios del Gobierno Nacional, expertos nacionales e internacionales, líderes gremiales, empresarios y trabajadores alrededor de un Sistema que se relaciona con la productividad y competitividad del país.

Aspectos como la movilidad social, redistribución del ingreso, calidad en la prestación del servicio de salud, formación para el trabajo, garantías de bienestar, oportunidades para los trabajadores y desafíos empresariales tendrán un espacio en la agenda académica.

Lo invitamos a participar en el Foro que permitirá trazar una hoja de ruta para fortalecer la Protección y Seguridad Social en Colombia a partir de diferentes visiones.

¡Los esperamos!


Adriana María Guillén Arango
Presidente Ejecutiva, Asocajas
